def unify_protein_names(adata, mapping_class=ProteinMapping):
    """Unify protein names in adata.var_names based on mapping."""
    """
    >>> adata = unify_protein_names(adata)
    >>> duplicates = pd.Series(var_names_list)[pd.Series(adata.var_names.tolist()).duplicated()]
    >>> print(f'duplicates')
    """
    rename_dict = {}
    for std, synonyms in mapping_class.MAPPING.items():
        for syn in synonyms:
            if syn in adata.var_names:
                rename_dict[syn] = std
    adata.var_names = [rename_dict.get(n, n) for n in adata.var_names]
    return adata