class DataSchema:
    """Controlled metadata schema with strict validation."""
    def describe(self):
        print("Available fields:\n")
        for field, mapping in side_maps.items():
            keys = [k for k in mapping.keys() if k != 'PAD']
            print(f"[{field}]")
            print("  " + ", ".join(keys))
            print()
        print("Note: if a value not in the allowed options is filled, it will be automatically replaced with 'PAD'.")

    def set(
        self,
        adata,
        Technology: str,
        Tissue_type: str,
        Health_status: str,
        Disease_type: str,
        auto_pad: bool = True
    ):
        assignments = {
            'Technology': Technology,
            'Tissue_type': Tissue_type,
            'Health_status': Health_status,
            'Disease_type': Disease_type
        }

        for field, value in assignments.items():
            allowed = [k for k in side_maps[field].keys() if k != 'PAD']

            if value not in allowed:
                if auto_pad:
                    print(f"'{value}' not in allowed values for {field}, setting to PAD")
                    adata.obs[field] = 'PAD'
                else:
                    raise ValueError(f"Invalid value '{value}' for {field}. Allowed: {allowed}")
            else:
                adata.obs[field] = value

    def interactive_fill(self, adata):
        print("Fill metadata (Ctrl+C to exit)\n")

        for field, mapping in side_maps.items():
            keys = [k for k in mapping.keys() if k != 'PAD']

            print(f"\n[{field}] options:")
            print(", ".join(keys))

            while True:
                val = input(f"{field}: ")
                if val in keys:
                    adata.obs[field] = val
                    break
                elif val == '':
                    adata.obs[field] = 'PAD'
                    break
                else:
                    print(f"Invalid value, allowed: {keys} or leave blank for PAD")



